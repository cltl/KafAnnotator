public class TagStat {

    private String tag;
    private Integer counts;

    public TagStat(String tag) {
        this.tag = tag;
        this.counts = 1;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public Integer getCounts() {
        return counts;
    }

    public void setCounts(Integer n) {
        this.counts = n;
    }

    public void increaseCounts() {
        this.counts++;
    }


}
